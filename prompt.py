def create_filter_prompt(listings_text):
    prompt = f"""
Ты — помощник, который помогает фильтровать квартиры по заданным критериям.
Вот список квартир:

{listings_text}

Критерии для отбора:
- Цена до 900€
- Тихий район (например, упоминание 'ruhig', 'ruhige Lage' или аналогичные выражения)
- Минимум 2 комнаты
- Желательно с балконом

Отметь, какие квартиры подходят под эти критерии, и коротко объясни почему.
Выведи номер квартиры и комментарий.
"""
    return prompt
